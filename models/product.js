const fs = require('fs');
const path = require('path');

const getDb = require('../util/database').getDb;

class Product{
  constructor(title, price, description, category, quantity, image){
    this.title = title;
    this.price = price;
    this.description = description;
    this.category = category;
    this.quantity = quantity;
    this.image = image;
  }

  save(){
    const db = getDb();
    return db.collection('products')
      .insertOne(this)
      .then(result => {
        console.log(result);
      })
      .catch(err => {
        console.log(err);
      });
  }
  static fetchAll(cb) {
    const db = getDb();
    return db
      .collection('products')
      .find()
      .toArray()
      .then(products => {
        console.log(products);
        return products;
      })
      .catch(err => {
        console.log(err);
      });
  }
}

module.exports = Product;

